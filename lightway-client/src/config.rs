#[serde_inline_default::serde_inline_default]
#[derive(Clone, Default, Parser, Debug, Deserialize, JsonSchema, Serialize, PartialEq)]
pub struct ConnectionConfig {
    /// Server to connect to in `<hostname>:<port>` format
    pub server: String,

    /// Connection mode
    #[serde(default)]
    pub mode: ConnectionType,

    /// Server domain name
    #[serde(default)]
    pub server_dn: Option<String>,

    /// Cipher to use for encryption
    #[serde(default)]
    pub cipher: Cipher,

    /// Username for User/Pass Auth
    #[serde(default)]
    pub username: Option<String>,

    /// Passwordfor User/Pass Auth
    #[serde(default)]
    pub password: Option<String>,

    /// Auth token
    #[serde(default)]
    pub token: Option<String>,

    /// Outside mtu
    #[serde_inline_default(MAX_OUTSIDE_MTU)]
    pub outside_mtu: usize,

    /// The CA Cert content or Path
    #[serde(default)]
    pub ca_cert: Option<String>,
}

impl ConnectionConfig {
    /// Try build auth from config
    #[cfg(feature = "mobile")]
    pub fn take_auth(&mut self) -> Result<AuthMethod, Error> {
        take_auth(
            self.token.take(),
            self.username.take(),
            self.password.take(),
        )
    }

    /// Try build CA from ca_crt
    #[cfg(feature = "mobile")]
    pub fn load_ca(&self) -> Result<lightway_core::wolfssl::RootCertificate<'_>, Error> {
        self.ca_cert
            .as_ref()
            .map(|ca| load_ca(ca))
            .ok_or(Error::InvalidCertificate)?
    }

    /// Try build SocketAddress from server field
    #[cfg(feature = "mobile")]
    pub fn skt_addr(&self) -> Result<std::net::SocketAddr, Error> {
        Ok(self
            .server
            .parse()
            .map_err(|_e| Error::InvalidSocketAddress)?)
    }
}

