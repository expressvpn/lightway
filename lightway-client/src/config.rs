impl Config {
    /// Try build auth from config
    pub fn take_auth(&mut self) -> Result<AuthMethod, Error> {
        take_auth(self.token.take(), self.user.take(), self.password.take())
    }

    /// Try build CA from ca_crt
    pub fn load_ca(&self) -> Result<lightway_core::wolfssl::RootCertificate<'_>, Error> {
        load_ca(&self.ca_cert)
    }

    /// Try build CA from ca_crt file
    /// If input ca_path is none, will take ca_cert field as path and pass to SSL
    pub fn load_ca_file<'a>(
        &self,
        ca_path: &'a mut Option<PathBuf>,
    ) -> lightway_core::wolfssl::RootCertificate<'a> {
        if ca_path.is_none() {
            *ca_path = Some(PathBuf::from(&self.ca_cert));
        }
        RootCertificate::PemFileOrDirectory(
            ca_path
                .as_ref()
                .expect("the path already initialized if it is none"),
        )
    }

    #[cfg(feature = "mobile")]
    pub fn apply_mobile_config(&mut self, config: MobileConfig) {
        match config.dns_ip.parse::<Ipv4Addr>() {
            Ok(ip) => self.tun_dns_ip = ip,
            Err(e) => tracing::warn!("dns ip is invalid: {e}. Using default."),
        }

        match config.local_ip.parse::<Ipv4Addr>() {
            Ok(ip) => self.tun_local_ip = ip,
            Err(e) => tracing::warn!("local ip is invalid: {e}. Using default."),
        }

        if let Ok(defer_interval) = u64::try_from(config.defer_timeout) {
            self.preferred_connection_wait_interval =
                Duration::from_std_duration(StdDuration::from_millis(defer_interval));
        } else {
            tracing::warn!(
                "Defer timeout value {} is invalid. Using default value.",
                config.defer_timeout
            );
        }

        let MobileConfig {
            enable_heart_beat,
            enable_expresslane,
            sni_header,
            ..
        } = config;

        self.keepalive_continuous = enable_heart_beat;
        self.enable_expresslane = enable_expresslane;
        self.sni_header = sni_header
    }

    #[cfg(feature = "mobile")]
    pub fn apply_mobile_connect_configs(&mut self, configs: Vec<MobileConnectionConfig>) {
        self.servers = configs.into_iter().map(|c| c.into()).collect()
    }
}

