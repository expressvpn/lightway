/// Endpoint settings from the mobile app, it is a small set and easier to use in mobile
#[cfg(feature = "mobile")]
#[derive(Clone, uniffi::Record)]
pub struct MobileConnectionConfig {
    pub server_ip: IpAddress,
    pub port: u16,
    pub server_dn: String,
    pub username: Option<String>,
    pub password: Option<String>,
    pub auth_token: Option<String>,
    pub ca_cert: String,
    pub use_tcp: bool,
    pub use_cha_cha_20: bool,
    pub outside_mtu: u32,
}

#[cfg(feature = "mobile")]
impl From<MobileConnectionConfig> for ConnectionConfig {
    fn from(
        MobileConnectionConfig {
            server_ip,
            port,
            server_dn,
            username,
            password,
            auth_token,
            ca_cert,
            use_tcp,
            use_cha_cha_20,
            outside_mtu,
        }: MobileConnectionConfig,
    ) -> ConnectionConfig {
        ConnectionConfig {
            server: format!("{}:{}", server_ip.ip.to_string(), port),
            mode: if use_tcp {
                ConnectionType::Tcp
            } else {
                ConnectionType::Udp
            },
            server_dn: if server_dn.trim().is_empty() {
                None
            } else {
                Some(server_dn)
            },
            cipher: if use_cha_cha_20 {
                Cipher::Chacha20
            } else {
                Cipher::default()
            },
            username,
            password,
            token: auth_token,
            outside_mtu: outside_mtu as usize,
            ca_cert: if ca_cert.trim().is_empty() {
                None
            } else {
                Some(ca_cert)
            },
            ..Default::default()
        }
    }
}

// Ref: https://mozilla.github.io/uniffi-rs/0.27/proc_macro/index.html#the-unifficustom_type-and-unifficustom_newtype-macros
#[cfg(feature = "mobile")]
uniffi::custom_type!(IpAddress, String);

#[cfg(feature = "mobile")]
#[derive(Debug, Eq, PartialEq, Clone)]
/// Custom type  with `String` as the `Builtin` bridge
pub struct IpAddress {
    pub ip: IpAddr,
}

#[cfg(feature = "mobile")]
impl std::fmt::Display for IpAddress {
    fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
        write!(f, "{}", self.ip)
    }
}

#[cfg(feature = "mobile")]
impl crate::UniffiCustomTypeConverter for IpAddress {
    type Builtin = String;

    fn into_custom(val: Self::Builtin) -> uniffi::Result<Self> {
        use std::str::FromStr;
        Ok(IpAddress {
            ip: IpAddr::from_str(val.as_str())?,
        })
    }

    fn from_custom(obj: Self) -> Self::Builtin {
        obj.ip.to_string()
    }
}

#[cfg(feature = "mobile")]
impl From<IpAddress> for IpAddr {
    fn from(val: IpAddress) -> Self {
        val.ip
    }
}

fn take_auth(
    token: Option<String>,
    user: Option<String>,
    password: Option<String>,
) -> Result<AuthMethod, Error> {
    match (token, user, password) {
        (Some(token), _, _) => Ok(AuthMethod::Token { token }),
        (_, Some(user), Some(password)) => Ok(AuthMethod::UserPass { user, password }),
        _ => Err(Error::InsufficientAuth),
    }
}

fn byte_size_schema(generator: &mut schemars::SchemaGenerator) -> schemars::Schema {
    let mut schema = String::json_schema(generator);
    schema.insert(
        "pattern".into(),
        "^[0-9]+(\\.[0-9]+)?\\s*(EB|EIB|GB|GIB|KB|KIB|MB|MIB|PB|PIB|TB|TIB|eb|eib|gb|gib|kb|kib|mb|mib|pb|pib|tb|tib)$".into(),
    );
    schema
}

