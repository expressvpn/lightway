#[cfg(feature = "mobile")]
impl From<MobileConnectionConfig> for ConnectionConfig {
    fn from(
        MobileConnectionConfig {
            server_ip,
            port,
            server_dn,
            username,
            password,
            auth_token,
            ca_cert,
            use_tcp,
            use_cha_cha_20,
            outside_mtu,
        }: MobileConnectionConfig,
    ) -> ConnectionConfig {
        ConnectionConfig {
            server: format!("{}:{}", server_ip.ip.to_string(), port),
            mode: if use_tcp {
                ConnectionType::Tcp
            } else {
                ConnectionType::Udp
            },
            server_dn: if server_dn.trim().is_empty() {
                None
            } else {
                Some(server_dn)
            },
            cipher: if use_cha_cha_20 {
                Cipher::Chacha20
            } else {
                Cipher::default()
            },
            username,
            password,
            token: auth_token,
            outside_mtu: outside_mtu as usize,
            ca_cert: if ca_cert.trim().is_empty() {
                None
            } else {
                Some(ca_cert)
            },
        }
    }
}

// Ref: https://mozilla.github.io/uniffi-rs/0.27/proc_macro/index.html#the-unifficustom_type-and-unifficustom_newtype-macros
#[cfg(all(feature = "mobile", not(feature = "mobile-test")))]
