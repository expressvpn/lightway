impl Config {
    /// Check ca input is a path
    fn use_ca_file(&self) -> bool {
        !self.ca_cert.starts_with("-----BEGIN CERTIFICATE-----")
    }

    /// Take out configures for servers
    /// and normalize the auth and ca of ConnectionConfig of servers
    pub fn take_servers(&mut self) -> Result<Vec<ConnectionConfig>, Error> {
        if self.servers.is_empty() {
            self.servers = vec![ConnectionConfig {
                server: self.server.clone(),
                mode: self.mode,
                server_dn: self.server_dn.take(),
                cipher: self.cipher,
                outside_mtu: self.outside_mtu,
                ..Default::default()
            }];
        }
        let ca_content = if self.use_ca_file() {
            // NOTE: only desktop support load ca from file path
            if cfg!(desktop) {
                std::fs::read_to_string(&self.ca_cert).ok()
            } else {
                None
            }
        } else {
            Some(self.ca_cert.clone())
        };
        for server in self.servers.iter_mut() {
            if server.username.is_none() && server.password.is_none() && server.token.is_none() {
                server.username = self.user.clone();
                server.password = self.password.clone();
                server.token = self.token.clone();
            }
            #[cfg(feature = "mobile")]
            {
                server.sni_header = self.sni_header.clone();
                server.enable_keepalive = self.keepalive_continuous;
                server.enable_expresslane = self.enable_expresslane;
            }

            if let Some(ref mut ca_cert) = server.ca_cert {
                if !ca_cert.starts_with("-----BEGIN CERTIFICATE-----") {
                    if cfg!(desktop) {
                        *ca_cert = std::fs::read_to_string(&mut *ca_cert)
                            .map_err(|_| Error::CaFileNotFound)?;
                    } else {
                        return Err(Error::InvalidCertificate);
                    }
                }
            } else {
                if ca_content.is_none() {
                    return Err(Error::CaFileNotFound);
                }
                server.ca_cert = ca_content.clone();
            }
        }
        Ok(std::mem::take::<Vec<ConnectionConfig>>(&mut self.servers))
    }

    #[cfg(feature = "mobile")]
    pub fn apply_mobile_config(&mut self, config: MobileConfig) {
        match config.dns_ip.parse::<Ipv4Addr>() {
            Ok(ip) => self.tun_dns_ip = ip,
            Err(e) => tracing::warn!("dns ip is invalid: {e}. Using default."),
        }

        match config.local_ip.parse::<Ipv4Addr>() {
            Ok(ip) => self.tun_local_ip = ip,
            Err(e) => tracing::warn!("local ip is invalid: {e}. Using default."),
        }

        if let Ok(defer_interval) = u64::try_from(config.defer_timeout) {
            self.preferred_connection_wait_interval =
                Duration::from_std_duration(StdDuration::from_millis(defer_interval));
        } else {
            tracing::warn!(
                "Defer timeout value {} is invalid. Using default value.",
                config.defer_timeout
            );
        }

        let MobileConfig {
            enable_heart_beat,
            enable_expresslane,
            sni_header,
            ..
        } = config;

        self.keepalive_continuous = enable_heart_beat;
        self.enable_expresslane = enable_expresslane;
        self.sni_header = sni_header
    }

    #[cfg(feature = "mobile")]
    pub fn apply_mobile_connect_configs(&mut self, configs: Vec<MobileConnectionConfig>) {
        self.servers = configs.into_iter().map(|c| c.into()).collect()
    }

    #[cfg(not(feature = "mobile"))]
    pub fn into_client_config(
        mut self,
        inside_io: Option<Arc<dyn crate::io::inside::InsideIO<()>>>,
    ) -> crate::ClientConfig<()> {
        assert!(
            self.servers.is_empty(),
            "Config should be normalized with `take_servers`"
        );

        let mut tun_config = lightway_app_utils::TunConfig::default();

        if let Some(tun_name) = self.tun_name.take() {
            tun_config.tun_name(tun_name);
        }

        #[cfg(windows)]
        if let Some(ref wintun_file) = self.wintun_file {
            tun_config.wintun_file(wintun_file);
        }

        // TODO: Fix in future PR
        tun_config
            .mtu(1350)
            .address(self.tun_local_ip.into())
            .destination(self.tun_peer_ip)
            .up();

        crate::ClientConfig {
            outside_mtu: self.outside_mtu,
            inside_io,
            tun_config,
            tun_local_ip: self.tun_local_ip,
            tun_peer_ip: self.tun_peer_ip,
            tun_dns_ip: self.tun_dns_ip,
            #[cfg(feature = "postquantum")]
            enable_pqc: self.enable_pqc,
            enable_expresslane: self.enable_expresslane,
            keepalive_interval: self.keepalive_interval.into(),
            keepalive_timeout: self.keepalive_timeout.into(),
            continuous_keepalive: self.keepalive_continuous,
            tracer_packet_timeout: self.tracer_packet_timeout.into(),
            preferred_connection_wait_interval: self.preferred_connection_wait_interval.into(),
            sndbuf: self.sndbuf,
            rcvbuf: self.rcvbuf,
            #[cfg(batch_receive)]
            enable_batch_receive: self.enable_batch_receive,
            #[cfg(desktop)]
            route_mode: self.route_mode,
            #[cfg(desktop)]
            dns_config_mode: self.dns_config_mode,
            enable_pmtud: self.enable_pmtud,
            pmtud_base_mtu: self.pmtud_base_mtu,
            #[cfg(feature = "io-uring")]
            enable_tun_iouring: self.enable_tun_iouring,
            #[cfg(feature = "io-uring")]
            iouring_entry_count: self.iouring_entry_count,
            #[cfg(feature = "io-uring")]
            iouring_sqpoll_idle_time: self.iouring_sqpoll_idle_time.into(),
            inside_pkt_codec_config: None,
            network_change_signal: None,
            best_connection_selected_signal: None,
            #[cfg(feature = "debug")]
            tls_debug: self.tls_debug,
            #[cfg(feature = "debug")]
            keylog: self.keylog,
        }
    }
    #[cfg(feature = "mobile")]
    pub fn into_client_config(
        self,
        tun_fd: std::os::fd::RawFd,
        external_event_handler: Arc<dyn crate::event_handlers::EventHandlers>,
        connected_index: Arc<std::sync::OnceLock<usize>>,
    ) -> crate::ClientConfig {
        let Config {
            tun_local_ip,
            tun_dns_ip,
            preferred_connection_wait_interval,
            enable_pmtud,
            ..
        } = self;
        crate::ClientConfig {
            tun_local_ip,
            tun_dns_ip,
            preferred_connection_wait_interval: preferred_connection_wait_interval.into(),
            internal_mtu: internal::INTERNAL_MTU,
            max_socket_buffer_len: internal::MAX_SOCKET_BUFFER_LEN,
            enable_pmtud,

            tun_fd,
            external_event_handler,
            connected_index,
        }
    }
}

