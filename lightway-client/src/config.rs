#[serde_inline_default::serde_inline_default]
#[derive(
    Clone, Default, Parser, Debug, Deserialize, JsonSchema, Serialize, PartialEq, Substrate,
)]
pub struct ConnectionConfig {
    /// Server to connect to in `<hostname>:<port>` format
    pub server: String,

    /// Connection mode
    #[serde(default)]
    pub mode: ConnectionType,

    /// Server domain name
    #[serde(default)]
    pub server_dn: Option<String>,

    /// Cipher to use for encryption
    #[serde(default)]
    pub cipher: Cipher,

    /// Username for User/Pass Auth
    #[serde(default)]
    pub user: Option<String>,

    /// Passwordfor User/Pass Auth
    #[serde(default)]
    pub password: Option<String>,

    /// Auth token
    #[serde(default)]
    pub token: Option<String>,

    /// Outside mtu
    #[serde_inline_default(MAX_OUTSIDE_MTU)]
    pub outside_mtu: usize,

    /// The CA Cert content or Path
    #[serde(default)]
    pub ca_cert: Option<String>,
}

impl ConnectionConfig {
    /// Try build auth from config
    pub fn take_auth(&mut self) -> Result<AuthMethod, Error> {
        take_auth(self.token.take(), self.user.take(), self.password.take())
    }

    /// Try build CA from ca_crt
    #[cfg(feature = "mobile")]
    pub fn load_ca(&self) -> Result<lightway_core::tls::RootCertificate<'_>, Error> {
        self.ca_cert
            .as_ref()
            .map(|ca| {
                if check_cert_header(ca) {
                    Ok(lightway_core::tls::RootCertificate::PemBuffer(
                        ca.as_bytes(),
                    ))
                } else {
                    Err(Error::InvalidCertificate)
                }
            })
            .ok_or(Error::InvalidCertificate)?
    }

    /// Try build SocketAddress from server field
    #[cfg(feature = "mobile")]
    pub fn skt_addr(&self) -> Result<std::net::SocketAddr, Error> {
        self.server
            .parse()
            .map_err(|_e| Error::InvalidSocketAddress)
    }
}

#[derive(Debug, thiserror::Error)]
#[cfg_attr(
    all(feature = "mobile", not(feature = "mobile-test")),
    derive(uniffi::Error)
)]
pub enum Error {
    /// Invalid network protocol
    #[error("Invalid network protocol")]
    #[allow(dead_code)]
    InvalidProtocol,

    /// Unable to load certificate
    #[error("Unable to load certificate")]
    InvalidCertificate,

    #[cfg(feature = "mobile")]
    /// Unable to parse the sokcet address
    #[error("Invalid Socket Address")]
    InvalidSocketAddress,

    /// Lack information to Authentication
    #[error("Insufficient information for Authentication")]
    InsufficientAuth,

    /// No such Ca file from user's input
    #[error("Ca file is absent")]
    CaFileNotFound,
}

fn take_auth(
    token: Option<String>,
    user: Option<String>,
    password: Option<String>,
) -> Result<AuthMethod, Error> {
    match (token, user, password) {
        (Some(token), _, _) => Ok(AuthMethod::VersionedToken {
            version: Version::MAXIMUM,
            token,
        }),
        (_, Some(user), Some(password)) => Ok(AuthMethod::UserPass { user, password }),
        _ => Err(Error::InsufficientAuth),
    }
}

fn check_cert_header<T: AsRef<str>>(content: T) -> bool {
    content.as_ref().starts_with("-----BEGIN CERTIFICATE-----")
}

fn byte_size_schema(generator: &mut schemars::SchemaGenerator) -> schemars::Schema {
    let mut schema = String::json_schema(generator);
    schema.insert(
        "pattern".into(),
        "^[0-9]+(\\.[0-9]+)?\\s*(EB|EIB|GB|GIB|KB|KIB|MB|MIB|PB|PIB|TB|TIB|eb|eib|gb|gib|kb|kib|mb|mib|pb|pib|tb|tib)$".into(),
    );
    schema
}

